package com.hanxia.duorougardon;

public class MemberBaseInfo {
	private String memberName;
	private String password;
	private String password_agian;
	private String mailbox;
	private String phoneNum;
	public String getMemberName() {
		return memberName;
	}
	public void setMemberName(String memberName) {
		this.memberName = memberName;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getPassword_agian() {
		return password_agian;
	}
	public void setPassword_agian(String password_agian) {
		this.password_agian = password_agian;
	}
	public String getMailbox() {
		return mailbox;
	}
	public void setMailbox(String mailbox) {
		this.mailbox = mailbox;
	}
	public String getPhoneNum() {
		return phoneNum;
	}
	public void setPhoneNum(String phoneNum) {
		this.phoneNum = phoneNum;
	}

}
